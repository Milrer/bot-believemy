let getMonth = (month) => {
    if (month === 1) return source.january;
    if (month === 2) return source.february;
    if (month === 3) return source.march;
    if (month === 4) return source.april;
    if (month === 5) return source.may;
    if (month === 6) return source.june;
    if (month === 7) return source.july;
    if (month === 8) return source.august;
    if (month === 9) return source.september;
    if (month === 10) return source.october;
    if (month === 11) return source.november;
    if (month === 12) return source.december;
};

let getEphemerisFix = (day, month) => {
    try {
        return getMonth(month)[day - 1].prefix;
    } catch (err) {}
};

let getEphemerisName = (day, month) => {
    try {
        return getMonth(month)[day - 1].name;
    } catch (err) {}
};

let getEphemerisNameWiki = (day, month) => {
    try {
        return getMonth(month)[day - 1].wiki;
        // const [name, prefix] = getMonth(month)[day - 1];
        // const nameWithUnderscores = name.replace(/\s+/g, '_');
        // return prefix ? `${prefix}_${name}` : nameWithUnderscores;
    } catch (error) {}
};

export function getTodayEphemerisName() {
    const today = new Date();

    return getEphemerisName(today.getDate(), today.getMonth() + 1);
}

export function getTodayEphemerisNameWiki() {
    const today = new Date();

    return getEphemerisNameWiki(today.getDate(), today.getMonth() + 1);
}

export function getConditionnal() {
    const today = new Date();

    return getEphemerisFix(today.getDate(), today.getMonth() + 1);
}
